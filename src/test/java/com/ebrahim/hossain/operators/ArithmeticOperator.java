public class ArithmeticOperator {

	public static void main(String[] args) {
		
		/*
		 * Arithmetic Operators
		 */
		
		int x = 100, y = 50;
        System.out.println("Used '+' operator : " +(x + y));
        System.out.println("Used '-' operator : " +(x - y));
        System.out.println("Used '*' operator : " +(x * y));
        System.out.println("Used '/' operator : " +(x / y));
        System.out.println("Used '%' operator : " +(x % y));
	}
}
